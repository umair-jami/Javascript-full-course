// rule for naming variable

// cannot start with number
//example:-
//1value--->invalid
//value1--->valid

var value1=20;
console.log(value1 * 10);
console.log(value1 ** 10);

//we can use only underscore and dollar sign
//first_name (valid)
//_firstname (valid)

var _firstname="raza"
console.log(_firstname) 

//first$name (valid)
//$firstname (valid)
var first$name="umair";
console.log(first$name)


